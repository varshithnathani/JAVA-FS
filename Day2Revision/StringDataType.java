package Day2Revision;

public class StringDataType {
    public static void main(String[] args) {
        String fName = "varshith";
        String lName = new String("Nathani");
        System.out.println(fName);
        System.out.println(lName);

        System.out.println(fName.length());
        System.out.println(lName.length());

        System.out.println(fName.charAt(0));

        System.out.println(fName.hashCode());

        System.out.println(fName.concat(" S/O Ashok"));

    }
}
